def addition(num1, num2)
  i = num1 + num2 
  return i
end

def subtraction(num1, num2)
  i = num1 - num2
  return i
end

def division(num1, num2)
  i = num1 / num2
  return i
end

def multiplication(num1, num2)
  i = num1 * num2
  return i
end

def modulo(num1, num2)
  i = num1 % num2
  return i
end

def square_root(num)
  i = Math.sqrt(num)
  return i
end

def order_of_operation(num1, num2, num3, num4)
  i =  num1 + (( num2 * num3 ) / num4 )
  return i
end
